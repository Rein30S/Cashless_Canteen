    private int getCountKantin(){
        int row = 0;
        try {
            rs = stm.executeQuery("SELECT COUNT(*) FROM toko");
            while(rs.next()){
                row =  rs.getInt(1);
            }
            rs.close();
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        return row;
    }

