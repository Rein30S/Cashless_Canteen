    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        // TODO add your handling code here:
        String usernameBaru = tfUsername.getText();
        String oldPass = tfPassLama.getText();
        String newPass = tfPassBaru.getText();
        
        if(!"".equals(usernameBaru) && !"".equals(oldPass)){
            try {
                rs = stm.executeQuery("SELECT * FROM user WHERE username = '"+usernameBaru+"' AND id_user !='"+user_login.getId_user()+"'");
                if(rs.next()){
                    JOptionPane.showMessageDialog(null, "Username sudah digunakan!");
                }else{
                    rs = stm.executeQuery("SELECT * FROM user WHERE username = '"+user_login.getusername()+"' AND password='"+oldPass+"'");
                    if(rs.next()){
                        int result = stm.executeUpdate("UPDATE user SET username = '"+usernameBaru+"' WHERE id_user='"+user_login.getId_user()+"'");
                        
                        if(!"".equals(newPass)){
                            stm.executeUpdate("UPDATE user SET password = '"+newPass+"' WHERE id_user='"+user_login.getId_user()+"'");
                        }
                        
                        if(result == 1){
                            JOptionPane.showMessageDialog(null, "Data berhasil di-update!");
                            
                            user_login.setusername(usernameBaru);
                            Menu_User mu = new Menu_User();
                            mu.setVisible(true);
                            this.dispose();
                        }else{
                            JOptionPane.showMessageDialog(null, "Data gagal di-update!");
                        }
                    }else{
                        JOptionPane.showMessageDialog(null, "Password yang Anda masukkan salah!");
                    }
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Kolom username & password lama harus diisi terlebih dahulu!");
        }
    }//GEN-LAST:event_jLabel6MouseClicked

